#class-design patter
# objects-real worldd entity
# reference-operations
class Person:
    def walk(self): #self=instance keyword (METHOD)
        print("Person is walking")
    def reading(self):
        print("person is reading")
#object
pe=Person() #created object
pe.walk()
pe.reading()
#object2
pe2=Person() #created object
pe2.walk()
pe2.reading()

